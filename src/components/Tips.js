                    <p>It's very, very important to train your team on this. They have to give the client a list of what they're going to do, then a longer timeframe than necessary. Why? Because when you fix the problem in less time than you stated, they know you went to bat for them. You'll have a loyal customer at that point. Take longer than you said and they're steaming mad.</p>

